package Models;

public class Contacto {
   String nombre;
   String apellido;
   String telefono;
   public Contacto (String nombre, String apellido, String telefono){

   } 
   public String getNombre() {
    return nombre;
}

   public String getApellido() {
    return apellido;
}

   public String getTelefono() {
    return telefono;
}

   
}
